
import 'package:flutter/material.dart';

class AppTextStyle {



  //////////////////////Black///////////////////////////
  static TextStyle cairo16RegularBlack000000(BuildContext context) {
    return TextStyle( // ignore: prefer_const_constructors
        fontSize:16,
        color: Colors.black,
        fontWeight: FontWeight.w400);
  }

  static TextStyle cairo16RegularGray585858(BuildContext context) {
    return const TextStyle(
        fontSize:16,
        // ignore: prefer_const_constructors
        color: Colors.grey,
        fontWeight: FontWeight.w400);
  }



}
